package assignments;

public class MathClass {

	public static void main(String[] args)
	{
		 System.out.println("To find the maximum of 2 nos using Math class *****************");
		 System.out.println("Max of 2 double nos  " +  Math.max(890.7654, 890.765321));
		 System.out.println("Max of 2 flost values  " + Math.max(56.8f, 67.5f));
		 System.out.println("Max of 2 integers  " + Math.max(23, 32));
		 System.out.println("Max of 2 long values " + Math.max(437643432,347545749));
          
		 System.out.println("To find the minimum of 2 nos using Math class *****************");
		 System.out.println("Min of 2 double nos  " +  Math.min(890.7654, 890.765321));
		 System.out.println("Min of 2 flost values  " + Math.min(56.8f, 67.5f));
		 System.out.println("Min of 2 integers  " + Math.min(23, 32));
		 System.out.println("Min of 2 long values  " + Math.min(437643432,347545749));

	}

}
